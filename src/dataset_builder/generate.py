def _truncate_patient_text(
    patient_text: str,
    tokenizer: AutoTokenizer,
    max_tokens: int = _MAX_PATIENT_TOKENS,
) -> str:
    """Truncate patient text to at most ``max_tokens`` tokens.

    Keeps the beginning (chief complaint) and end (questions) of the
    text, joining them with an ellipsis when truncation is needed.
    This ensures the model sees the core medical query even for very
    long patient messages.
    """
    token_ids = tokenizer.encode(patient_text, add_special_tokens=False)
    if len(token_ids) <= max_tokens:
        return patient_text

    # Keep first 60% and last 40% of the budget
    head_budget = int(max_tokens * 0.6)
    tail_budget = max_tokens - head_budget
    head = tokenizer.decode(token_ids[:head_budget], skip_special_tokens=True).strip()
    tail = tokenizer.decode(token_ids[-tail_budget:], skip_special_tokens=True).strip()
    return f"{head} [...] {tail}"


def _build_prompt(patient_text: str) -> str:
    """Build the prompt fed to the base LLM for generating a rejected response.

    Includes a system instruction to steer the model into the doctor
    role and prevent it from continuing the patient narrative.
    """
    return f"{_SYSTEM_INSTRUCTION}[Patient]: {patient_text}\n[Doctor]:"


def _contains_any(text: str, phrases: list[str]) -> bool:
    return any(p in text for p in phrases)


def _has_repeated_sentence(text: str) -> bool:
    """Detect low-quality degenerate generation via repeated long sentences."""
    sentences = [
        clean_text(s).lower()
        for s in re.split(r"[.!?]\s+", text)
        if len(clean_text(s).split()) >= 6
    ]
    if len(sentences) < 2:
        return False
    counts = Counter(sentences)
    return max(counts.values()) > 1


def _doctor_text_from_pair(pair: dict, field: str) -> str:
    """Extract doctor text from pair dict, preferring explicit doctor-only field."""
    doctor_key = f"{field}_doctor"
    if pair.get(doctor_key):
        return clean_text(str(pair[doctor_key]))
    full_text = clean_text(str(pair.get(field, "")))
    return clean_text(full_text.split("[Doctor]:", 1)[-1])


def _heuristic_quality_score(
    response_text: str, patient_text: str | None = None
) -> float:
    """Estimate response quality for pairwise preference filtering."""
    text = clean_text(response_text)
    if not text:
        return -1.0

    lowered = text.lower()
    n_words = len(text.split())
    score = 0.0

    # Length prior: very short or very long outputs are usually weak.
    if n_words < 8:
        score -= 0.90
    elif n_words < 20:
        score -= 0.20
    elif n_words <= _MAX_REJECTED_WORDS:
        score += 0.35
    else:
        score -= min(0.60, (n_words - _MAX_REJECTED_WORDS) / 300.0)

    score += min(n_words, 140) / 700.0

    med_hits = sum(1 for hint in _MEDICAL_CONTENT_HINTS if hint in lowered)
    score += min(0.35, med_hits * 0.06)
    reason_hits = sum(1 for hint in _REASONING_HINTS if hint in lowered)
    score += min(0.25, reason_hits * 0.05)

    if _JUDGE_PLACEHOLDER_RE.search(text):
        score -= 0.80
    if _contains_any(lowered, _REFUSAL_OR_META_PHRASES):
        score -= 0.60
    if _contains_any(lowered, _JUDGE_BAD_PATTERNS):
        score -= 0.45
    if _has_repeated_sentence(text):
        score -= 0.35
    if "[patient]:" in lowered or "[doctor]:" in lowered:
        score -= 0.40
    if "###" in lowered:
        score -= 0.30

    # Penalize over-urgent triage on low-risk prompts.
    if patient_text and _contains_any(lowered, _URGENT_ADVICE_PHRASES):
        if not _contains_any(patient_text.lower(), _PATIENT_HIGH_RISK_CUES):
            score -= 0.20

    return score


def filter_by_judge(
    pairs: list[dict],
    min_margin: float = 0.20,
    min_chosen_score: float = 0.15,
    add_scores: bool = True,
) -> list[dict]:
    """Heuristic pairwise judge to keep only confident chosen>rejected pairs.

    Parameters
    ----------
    pairs:
        Preference pairs with chosen/rejected fields.
    min_margin:
        Minimum required score margin ``chosen_score - rejected_score``.
    min_chosen_score:
        Minimum absolute quality score for chosen response.
    add_scores:
        If ``True``, append judge score fields to kept rows.
    """
    if not pairs:
        return pairs
    if min_margin < 0:
        raise ValueError("min_margin must be >= 0.")

    kept: list[dict] = []
    dropped_low_chosen = 0
    dropped_low_margin = 0

    for pair in tqdm(pairs, desc="Judge filtering", unit="pair"):
        patient_text = clean_text(str(pair.get("prompt", "")))
        chosen_text = _doctor_text_from_pair(pair, "chosen")
        rejected_text = _doctor_text_from_pair(pair, "rejected")
        if not chosen_text or not rejected_text:
            dropped_low_chosen += 1
            continue

        chosen_score = _heuristic_quality_score(chosen_text, patient_text)
        rejected_score = _heuristic_quality_score(rejected_text, patient_text)
        margin = chosen_score - rejected_score

        if chosen_score < min_chosen_score:
            dropped_low_chosen += 1
            continue
        if margin < min_margin:
            dropped_low_margin += 1
            continue

        if add_scores:
            pair["judge_score_chosen"] = round(chosen_score, 4)
            pair["judge_score_rejected"] = round(rejected_score, 4)
            pair["judge_margin"] = round(margin, 4)
        kept.append(pair)

    n_dropped = len(pairs) - len(kept)
    print(
        f"Judge filter: removed {n_dropped}/{len(pairs)} pairs "
        f"(low_chosen={dropped_low_chosen}, low_margin={dropped_low_margin}, "
        f"min_margin={min_margin:.2f}), kept {len(kept)}"
    )
    log.info(
        "Judge filter: removed %d/%d (low_chosen=%d, low_margin=%d, min_margin=%.2f)",
        n_dropped,
        len(pairs),
        dropped_low_chosen,
        dropped_low_margin,
        min_margin,
    )
    return kept


def _is_valid_doctor_response(text: str, patient_text: str | None = None) -> bool:
    """Check whether generated text looks like a doctor response.

    Returns ``False`` if the text appears to continue the patient
    narrative rather than providing a medical response.
    """
    text = clean_text(text)
    lowered = text.lower()
    n_words = len(text.split())
    if not text or n_words < _MIN_REJECTED_WORDS or n_words > _MAX_REJECTED_WORDS:
        return False

    if "[patient]:" in lowered or "[doctor]:" in lowered:
        return False
    if _contains_any(lowered, _REFUSAL_OR_META_PHRASES):
        return False

    # Check leading text (first 60 chars) for patient-voice phrases
    lead = lowered[:80].strip()

    # Strong signal: starts with first-person patient continuation
    if any(lead.startswith(p) for p in _PATIENT_CONTINUATION_PHRASES):
        return False

    # Check first sentence for heavy first-person narration
    first_sentence = lowered.split(".")[0] if "." in lowered else lowered[:120]
    i_count = first_sentence.split().count("i")
    if i_count >= 3:
        return False

    if _has_repeated_sentence(text):
        return False

    # Over-urgent triage on non-acute prompts is usually low-quality noise.
    if patient_text and _contains_any(lowered, _URGENT_ADVICE_PHRASES):
        patient_lower = patient_text.lower()
        if not _contains_any(patient_lower, _PATIENT_HIGH_RISK_CUES):
            return False

    return True


def _sort_by_prompt_length(
    examples: list[dict],
    tokenizer: AutoTokenizer,
) -> tuple[list[dict], list[int]]:
    """Sort examples by tokenized prompt length (descending).

    Grouping similar-length prompts together minimises padding waste
    and maximises GPU utilisation on batched generation.

    Returns the sorted list and the original indices so results can be
    un-shuffled later.
    """
    lengths = [
        len(
            tokenizer.encode(
                _build_prompt(ex.get("_patient_truncated", ex["patient"])),
                add_special_tokens=False,
            )
        )
        for ex in examples
    ]
    order = sorted(range(len(examples)), key=lambda i: lengths[i], reverse=True)
    sorted_examples = [examples[i] for i in order]
    return sorted_examples, order


@torch.inference_mode()
def generate_rejected_responses(
    examples: list[dict],
    model_name: str,
    batch_size: int = 32,
    max_new_tokens: int = 256,
    temperature: float = 0.7,
    top_p: float = 0.95,
    device: str | None = None,
    load_in_4bit: bool = False,
    use_flash_attention: bool = False,
    checkpoint_dir: Path | str | None = None,
    checkpoint_every: int = 500,
) -> list[dict]:
    """Generate one rejected response per example using a base LLM.

    Optimised for high-end GPUs (H200 / A100 / 4090):
    - BF16 precision (native on H200).
    - Flash Attention 2 (falls back to SDPA if unavailable).
    - Sorted-by-length batching to minimise padding waste.
    - Large default batch size (32, tune up/down for your VRAM).
    - Optional 4-bit quantization via ``bitsandbytes``.
    - Periodic JSONL checkpointing to ``checkpoint_dir``.
    - Throughput reporting (examples/sec, tokens/sec).

    Parameters
    ----------
    examples:
        List of dicts with ``patient`` and ``doctor`` keys.
    model_name:
        HuggingFace model id for a **base** (not instruction-tuned) model,
        e.g. ``mistralai/Mistral-7B-v0.1``, ``meta-llama/Llama-3-8B``.
    batch_size:
        Batch size for generation.  H200 80GB can handle 32-64 for 7B
        models in bf16.  Reduce if OOM.
    max_new_tokens:
        Maximum tokens to generate. Capped per-batch to roughly match
        the gold response length.
    temperature:
        Sampling temperature (0.7 recommended for conservative rejected).
    top_p:
        Nucleus sampling probability.
    device:
        Device string.  Auto-detected if ``None``.
    load_in_4bit:
        If ``True``, load model in NF4 4-bit quantization via
        bitsandbytes.  Reduces VRAM usage ~4× at the cost of some
        quality — useful for larger models (13B+) or smaller GPUs.
    checkpoint_dir:
        If set, write intermediate results every ``checkpoint_every``
        batches so work is not lost on crashes.
    checkpoint_every:
        Batches between checkpoint writes.

    Returns
    -------
    List of dicts with keys:
        ``conversation_id``, ``prompt``, ``chosen``, ``rejected``,
        ``chosen_doctor``, ``rejected_doctor``.
    """
    if device is None:
        device = "cuda" if torch.cuda.is_available() else "cpu"

    log.info("Loading base model %s on %s …", model_name, device)
    print(f"Loading base model: {model_name} on {device}")
    lowered_model_name = model_name.lower()
    if any(tag in lowered_model_name for tag in ["instruct", "chat", "-it", "sft"]):
        print(
            "WARNING: `base_model` name looks instruction/chat-tuned. "
            "For cleaner negatives, prefer a true base checkpoint."
        )

    # ── Tokenizer ────────────────────────────────────────────────────────
    tokenizer = AutoTokenizer.from_pretrained(model_name, trust_remote_code=True)
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token
    tokenizer.padding_side = "left"  # required for batched causal generation

    # ── Model loading (optimised for H200) ───────────────────────────────
    model_kwargs: dict = {
        "trust_remote_code": True,
        "low_cpu_mem_usage": True,
    }

    if device != "cpu":
        model_kwargs["device_map"] = "auto"

        # BF16 is native on H200/A100; fall back to FP16 on older GPUs
        if torch.cuda.is_bf16_supported():
            model_kwargs["dtype"] = torch.bfloat16
        else:
            model_kwargs["dtype"] = torch.float16

        # Flash Attention 2 — massive speedup for long sequences
        if use_flash_attention:
            try:
                import flash_attn  # noqa: F401

                model_kwargs["attn_implementation"] = "flash_attention_2"
                print("Using Flash Attention 2")
            except ImportError:
                model_kwargs["attn_implementation"] = "sdpa"
                print("flash-attn not installed — falling back to PyTorch SDPA")
        else:
            model_kwargs["attn_implementation"] = "sdpa"
            print("Using PyTorch SDPA")

        # Optional 4-bit quantization
        if load_in_4bit:
            try:
                import bitsandbytes  # noqa: F401
            except ImportError:
                print(
                    "WARNING: --load_in_4bit requested but bitsandbytes is not installed. "
                    "Continuing without 4-bit quantization."
                )
                load_in_4bit = False

        if load_in_4bit:
            model_kwargs["quantization_config"] = BitsAndBytesConfig(
                load_in_4bit=True,
                bnb_4bit_quant_type="nf4",
                bnb_4bit_compute_dtype=model_kwargs["dtype"],
                bnb_4bit_use_double_quant=True,
            )
            print("Loading model in NF4 4-bit quantization")
    else:
        model_kwargs["dtype"] = torch.float32

    model = AutoModelForCausalLM.from_pretrained(model_name, **model_kwargs)
    if device == "cpu":
        model = model.to(device)
    model.eval()

    # Pre-compile with torch.compile for extra speed (PyTorch ≥2.0)
    if device != "cpu" and hasattr(torch, "compile"):
        try:
            model = torch.compile(model, mode="reduce-overhead")
            print("Model compiled with torch.compile (reduce-overhead)")
        except Exception as exc:
            log.warning("torch.compile failed, continuing without: %s", exc)

    # ── Pre-truncate patient texts for generation prompts ───────────────
    log.info("Truncating long patient texts (max %d tokens)…", _MAX_PATIENT_TOKENS)
    for ex in examples:
        ex["_patient_truncated"] = _truncate_patient_text(
            ex["patient"], tokenizer, _MAX_PATIENT_TOKENS
        )

    # ── Sort examples by prompt length for efficient batching ───────────
    sorted_examples, _ = _sort_by_prompt_length(examples, tokenizer)

    # ── Checkpoint setup ────────────────────────────────────────────────
    ckpt_path: Path | None = None
    if checkpoint_dir:
        ckpt_path = Path(checkpoint_dir) / "preference_pairs_checkpoint.jsonl"
        ckpt_path.parent.mkdir(parents=True, exist_ok=True)

    # ── Generation loop ─────────────────────────────────────────────────
    results: list[dict] = []
    skipped = 0
    total_generated_tokens = 0
    t_start = time.perf_counter()
    n_batches = (len(sorted_examples) + batch_size - 1) // batch_size

    pbar = tqdm(
        range(0, len(sorted_examples), batch_size),
        desc="Generating rejected",
        total=n_batches,
        unit="batch",
    )

    for batch_idx, start in enumerate(pbar):
        batch = sorted_examples[start : start + batch_size]
        prompts = [_build_prompt(ex["_patient_truncated"]) for ex in batch]

        # Per-batch max tokens: match longest gold response (×1.2), clamped
        gold_lengths = [len(ex["doctor"].split()) for ex in batch]
        batch_max_tokens = min(
            max_new_tokens,
            max(int(max(gold_lengths) * 1.2), 64),
        )

        encoded = tokenizer(
            prompts,
            return_tensors="pt",
            padding=True,
            truncation=True,
            max_length=1024,
        ).to(model.device)

        output_ids = model.generate(
            **encoded,
            max_new_tokens=batch_max_tokens,
            temperature=temperature,
            top_p=top_p,
            do_sample=True,
            pad_token_id=tokenizer.pad_token_id,
        )

        # Decode only the generated tokens (strip the prompt)
        for i, ex in enumerate(batch):
            input_len = encoded["input_ids"][i].shape[0]
            generated_ids = output_ids[i][input_len:]
            total_generated_tokens += len(generated_ids)

            rejected_text = tokenizer.decode(
                generated_ids, skip_special_tokens=True
            ).strip()

            # Truncate at the first line that looks like a new speaker turn
            for stop in [
                "\n[Patient]",
                "\n[Doctor]",
                "\nPatient:",
                "\nDoctor:",
            ]:
                if stop in rejected_text:
                    rejected_text = rejected_text[: rejected_text.index(stop)].strip()

            rejected_text = clean_text(rejected_text)

            # Skip responses that are too short or look like patient
            # narrative continuations rather than doctor responses
            if not _is_valid_doctor_response(rejected_text, ex["patient"]):
                skipped += 1
                continue

            results.append(
                {
                    "conversation_id": ex["conversation_id"],
                    "source_conversation_id": ex.get(
                        "source_conversation_id", ex["conversation_id"]
                    ),
                    "prompt": format_dialogue(ex["patient"]),
                    "chosen": format_dialogue(ex["patient"], ex["doctor"]),
                    "rejected": format_dialogue(ex["patient"], rejected_text),
                    "chosen_doctor": clean_text(ex["doctor"]),
                    "rejected_doctor": rejected_text,
                }
            )

        # Update progress bar with throughput info
        elapsed = time.perf_counter() - t_start
        ex_per_sec = (start + len(batch)) / elapsed if elapsed > 0 else 0
        tok_per_sec = total_generated_tokens / elapsed if elapsed > 0 else 0
        pbar.set_postfix(
            kept=len(results),
            skipped=skipped,
            ex_s=f"{ex_per_sec:.1f}",
            tok_s=f"{tok_per_sec:.0f}",
        )

        # Periodic checkpoint
        if ckpt_path and (batch_idx + 1) % checkpoint_every == 0:
            write_jsonl(ckpt_path, results)
            log.info(
                "Checkpoint saved: %d pairs at batch %d", len(results), batch_idx + 1
            )

    elapsed = time.perf_counter() - t_start
    print(
        f"Generated {len(results)} rejected responses from {len(examples)} examples "
        f"(skipped {skipped} short/empty) in {elapsed:.1f}s "
        f"({len(examples) / elapsed:.1f} ex/s, {total_generated_tokens / elapsed:.0f} tok/s)"
    )

    # Free base-model VRAM before embedding-based filtering.
    if device != "cpu" and torch.cuda.is_available():
        del model
        torch.cuda.empty_cache()

    return results


# Similarity filtering


def filter_by_similarity(
    pairs: list[dict],
    threshold: float = 0.90,
    keep_high_sim_fraction: float = 0.05,
    model_name: str = "sentence-transformers/all-MiniLM-L6-v2",
    batch_size: int = 64,
    device: str | None = None,
) -> list[dict]:
    """Remove pairs where chosen and rejected are too similar.

    Uses cosine similarity of sentence embeddings. Pairs with
    similarity >= ``threshold`` are usually discarded, but an optional
    fraction can be retained as hard negatives.

    Parameters
    ----------
    pairs:
        Preference pairs with ``chosen`` and ``rejected`` keys.
    threshold:
        Cosine similarity cutoff.  Pairs at or above this are removed.
    keep_high_sim_fraction:
        Fraction of above-threshold pairs to keep as hard negatives
        (keeps those closest to threshold).
    model_name:
        Sentence-transformer model for embeddings.
    batch_size:
        Encoding batch size.
    device:
        Device string.  Auto-detected if ``None``.

    Returns
    -------
    Filtered list of preference pairs.
    """
    if not pairs:
        return pairs
    if threshold < 0 or threshold > 1:
        raise ValueError("threshold must be in [0, 1].")
    if keep_high_sim_fraction < 0 or keep_high_sim_fraction > 1:
        raise ValueError("keep_high_sim_fraction must be in [0, 1].")

    if device is None:
        device = "cuda" if torch.cuda.is_available() else "cpu"

    try:
        from sentence_transformers import SentenceTransformer
    except ImportError:
        log.warning(
            "sentence-transformers not installed — skipping similarity filter.  "
            "Install with: pip install sentence-transformers"
        )
        print(
            "WARNING: sentence-transformers not installed, skipping similarity filter."
        )
        return pairs

    print(f"Loading embedding model: {model_name}")
    embed_model = SentenceTransformer(model_name, device=device)

    valid_pairs: list[dict] = []
    chosen_texts: list[str] = []
    rejected_texts: list[str] = []

    for p in pairs:
        chosen_text = clean_text(
            str(p.get("chosen_doctor") or p.get("chosen", "").split("[Doctor]:", 1)[-1])
        )
        rejected_text = clean_text(
            str(
                p.get("rejected_doctor")
                or p.get("rejected", "").split("[Doctor]:", 1)[-1]
            )
        )
        if not chosen_text or not rejected_text:
            continue
        valid_pairs.append(p)
        chosen_texts.append(chosen_text)
        rejected_texts.append(rejected_text)

    n_empty = len(pairs) - len(valid_pairs)
    if not valid_pairs:
        print("WARNING: no valid chosen/rejected texts found after preprocessing.")
        return []

    print("Encoding chosen responses …")
    chosen_embs = embed_model.encode(
        chosen_texts,
        batch_size=batch_size,
        show_progress_bar=True,
        convert_to_tensor=True,
        normalize_embeddings=True,
    )
    print("Encoding rejected responses …")
    rejected_embs = embed_model.encode(
        rejected_texts,
        batch_size=batch_size,
        show_progress_bar=True,
        convert_to_tensor=True,
        normalize_embeddings=True,
    )

    # Cosine similarity (embeddings are already normalized)
    sims = torch.sum(chosen_embs * rejected_embs, dim=1).cpu().tolist()

    high_sim_indices = [i for i, sim in enumerate(sims) if sim >= threshold]
    keep_high_indices: set[int] = set()
    if keep_high_sim_fraction > 0 and high_sim_indices:
        n_keep_high = max(1, int(len(high_sim_indices) * keep_high_sim_fraction))
        # Keep the hardest negatives closest to the threshold (not near-duplicates).
        keep_high_indices = set(
            sorted(high_sim_indices, key=lambda i: sims[i])[:n_keep_high]
        )

    filtered = [
        p
        for i, p in enumerate(valid_pairs)
        if sims[i] < threshold or i in keep_high_indices
    ]
    n_removed_similarity = sum(
        1
        for i in range(len(valid_pairs))
        if sims[i] >= threshold and i not in keep_high_indices
    )
    n_removed_total = n_removed_similarity + n_empty

    print(
        f"Similarity filter: removed {n_removed_total}/{len(pairs)} pairs "
        f"(threshold={threshold:.2f}, kept_high_sim={len(keep_high_indices)}, "
        f"dropped_empty={n_empty}), kept {len(filtered)}"
    )
    log.info(
        "Similarity filter: removed %d/%d pairs (threshold=%.2f, kept_high_sim=%d, dropped_empty=%d)",
        n_removed_total,
        len(pairs),
        threshold,
        len(keep_high_indices),
        n_empty,
    )

    return filtered


# Train / val / test splitting


def split_data(
    examples: list[dict], seed: int = 42
) -> tuple[list[dict], list[dict], list[dict]]:
    """Group-aware split into 80/10/10 train/val/test by source conversation."""
    if not examples:
        return [], [], []

    def _group_id(ex: dict) -> str:
        src = clean_text(str(ex.get("source_conversation_id", "")))
        if src:
            return src
        conv_id = clean_text(str(ex.get("conversation_id", "")))
        if "_" in conv_id:
            base, suffix = conv_id.rsplit("_", 1)
            if suffix.isdigit():
                return base
        return conv_id

    rng = random.Random(seed)
    grouped: dict[str, list[dict]] = defaultdict(list)
    for ex in examples:
        grouped[_group_id(ex)].append(ex)

    group_ids = list(grouped.keys())
    rng.shuffle(group_ids)

    n_groups = len(group_ids)
    n_train = int(0.8 * n_groups)
    n_val = int(0.1 * n_groups)

    train: list[dict] = []
    val: list[dict] = []
    test: list[dict] = []

    for g in group_ids[:n_train]:
        train.extend(grouped[g])
    for g in group_ids[n_train : n_train + n_val]:
        val.extend(grouped[g])
    for g in group_ids[n_train + n_val :]:
        test.extend(grouped[g])

    rng.shuffle(train)
    rng.shuffle(val)
    rng.shuffle(test)

    return (
        train,
        val,
        test,
    )


# I/O


def write_jsonl(path: Path, rows: list[dict]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")


