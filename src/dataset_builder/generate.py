# Text helpers


def clean_text(text: str) -> str:
    """Normalize whitespace and strip lightweight artifacts."""
    text = re.sub(r"\s+", " ", text or "")
    text = text.replace("\u200b", "").replace("\ufeff", "")
    return text.strip()


def format_dialogue(patient_text: str, doctor_text: str | None = None) -> str:
    """Format a single patient-doctor exchange for model input."""
    patient_text = clean_text(patient_text)
    doctor_text = clean_text(doctor_text or "")
    if doctor_text:
        return f"[Patient]: {patient_text}\n[Doctor]: {doctor_text}"
    return f"[Patient]: {patient_text}\n[Doctor]:"


# Dataset loading


def _load_dataset(name: str | None, subset: str | None) -> Dataset:
    """Load a HuggingFace dataset."""
    if name:
        if subset:
            return load_dataset(name, subset, split="train")  # type: ignore[arg-type]
        return load_dataset(name, split="train")  # type: ignore[arg-type]

    raise ValueError("Dataset name must be provided.")


def _extract_pairs(record: dict) -> list[tuple[str, str]]:
    """Extract (patient, doctor) text pairs from a MedDialog record.

    Handles multiple schemas:
    - Flat columns: ``Patient``/``Doctor``, ``query``/``response``, etc.
    - Multi-turn with role labels: list of dicts with ``role``+``text``.
    - Alternating utterances: plain list of strings where even indices
      are patient turns and odd indices are doctor turns (bigbio/meddialog).
    """
    keys = {k.lower(): k for k in record}

    # --- Schema 1: flat Patient/Doctor or query/response columns ----------
    q_key = next(
        (
            keys[k]
            for k in keys
            if k in {"query", "question", "patient", "patient_message"}
        ),
        None,
    )
    a_key = next(
        (
            keys[k]
            for k in keys
            if k in {"response", "answer", "doctor", "doctor_response"}
        ),
        None,
    )
    if q_key and a_key:
        p = clean_text(str(record.get(q_key, "")))
        d = clean_text(str(record.get(a_key, "")))
        if p and d:
            return [(p, d)]

    # --- Schema 2 & 3: conversation / dialogue list -----------------------
    conv_key = next(
        (
            keys[k]
            for k in keys
            if k in {"dialog", "dialogue", "conversation", "utterances"}
        ),
        None,
    )
    if conv_key:
        conv = record.get(conv_key, [])
        if not conv:
            return []

        # Check if turns carry role metadata (list of dicts)
        if isinstance(conv[0], dict):
            pairs: list[tuple[str, str]] = []
            pending: str | None = None
            for turn in conv:
                role = str(turn.get("role") or turn.get("speaker") or "").lower()
                text = clean_text(
                    str(
                        turn.get("text")
                        or turn.get("utterance")
                        or turn.get("content")
                        or ""
                    )
                )
                if not text:
                    continue
                if role in {"patient", "user", "questioner"}:
                    pending = text
                elif role in {"doctor", "assistant", "answerer"} and pending:
                    pairs.append((pending, text))
                    pending = None
            return pairs

        # Alternating utterances (plain strings): even = patient, odd = doctor
        # This is the bigbio/meddialog format.
        pairs = []
        for i in range(0, len(conv) - 1, 2):
            p = clean_text(str(conv[i]))
            d = clean_text(str(conv[i + 1]))
            if p and d:
                pairs.append((p, d))
        return pairs

    return []


def load_dialogues(
    dataset_name: str | None,
    dataset_subset: str | None,
    max_samples: int | None,
) -> list[dict]:
    """Return list of dialogue rows for SFT/RM data generation."""
    ds = _load_dataset(dataset_name, dataset_subset)
    examples: list[dict] = []
    for idx, record in enumerate(ds):
        conv_id = str(record.get("conversation_id") or record.get("id") or idx)
        for p_idx, (patient, doctor) in enumerate(_extract_pairs(record)):
            examples.append(
                {
                    "conversation_id": f"{conv_id}_{p_idx}",
                    "source_conversation_id": conv_id,
                    "patient": patient,
                    "doctor": doctor,
                }
            )
            if max_samples and len(examples) >= max_samples:
                break
        if max_samples and len(examples) >= max_samples:
            break
    return examples


def _extract_patient_from_prompt(prompt_text: str) -> str:
    prompt_text = clean_text(prompt_text)
    if not prompt_text:
        return ""
    if "[Doctor]:" in prompt_text:
        patient_part = prompt_text.split("[Doctor]:", 1)[0]
    else:
        patient_part = prompt_text
    if "[Patient]:" in patient_part:
        patient_part = patient_part.split("[Patient]:", 1)[1]
    return clean_text(patient_part)


def _extract_patient_from_dialogue(text: str) -> str:
    text = clean_text(text)
    if not text:
        return ""
    if "[Doctor]:" in text:
        patient_part = text.split("[Doctor]:", 1)[0]
    else:
        patient_part = text
    if "[Patient]:" in patient_part:
        patient_part = patient_part.split("[Patient]:", 1)[1]
    return clean_text(patient_part)


def _normalize_local_row(row: dict, idx: int) -> dict | None:
    """Normalize local JSONL rows into ``{conversation_id, patient, doctor}``."""
    patient = clean_text(str(row.get("patient") or ""))
    if not patient:
        patient = _extract_patient_from_prompt(str(row.get("prompt") or ""))
    if not patient:
        patient = _extract_patient_from_dialogue(str(row.get("text") or ""))

    doctor = clean_text(str(row.get("doctor") or ""))
    if not doctor:
        doctor = clean_text(str(row.get("chosen_doctor") or ""))
    if not doctor:
        doctor = clean_text(str(row.get("text") or "").split("[Doctor]:", 1)[-1])
    if not doctor:
        doctor = clean_text(str(row.get("chosen") or "").split("[Doctor]:", 1)[-1])

    if not patient or not doctor:
        return None

    conv_id = clean_text(
        str(row.get("conversation_id") or row.get("id") or f"local_{idx}")
    )
    source_id = clean_text(str(row.get("source_conversation_id") or ""))
    if not source_id:
        if "_" in conv_id:
            base, suffix = conv_id.rsplit("_", 1)
            if suffix.isdigit():
                source_id = base
        if not source_id:
            source_id = conv_id

    return {
        "conversation_id": conv_id,
        "source_conversation_id": source_id,
        "patient": patient,
        "doctor": doctor,
    }


def _read_local_jsonl(path: Path, max_rows: int | None = None) -> list[dict]:
    rows: list[dict] = []
    with path.open("r", encoding="utf-8") as f:
        for idx, line in enumerate(f):
            if max_rows and len(rows) >= max_rows:
                break
            line = line.strip()
            if not line:
                continue
            try:
                row = json.loads(line)
            except json.JSONDecodeError:
                continue
            if not isinstance(row, dict):
                continue
            normalized = _normalize_local_row(row, idx)
            if normalized:
                rows.append(normalized)
    return rows


def _resolve_split_path(
    deidentified_dir: Path,
    split: str,
    deidentified_prefix: str,
) -> Path:
    candidates = [
        deidentified_dir / f"{deidentified_prefix}_{split}_deidentified.jsonl",
        deidentified_dir / f"{deidentified_prefix}_{split}.jsonl",
        deidentified_dir / f"meddialog_{split}_deidentified.jsonl",
        deidentified_dir / f"meddialog_{split}.jsonl",
    ]
    for path in candidates:
        if path.exists():
            return path
    raise FileNotFoundError(
        f"Missing {split} split in {deidentified_dir}. Tried: "
        + ", ".join(str(p.name) for p in candidates)
    )


def load_deidentified_splits(
    deidentified_dir: str | Path,
    deidentified_prefix: str,
    max_samples: int | None,
) -> tuple[list[dict], list[dict], list[dict]]:
    """Load train/val/test splits from local de-identified JSONL files."""
    base = Path(deidentified_dir)
    train_path = _resolve_split_path(base, "train", deidentified_prefix)
    val_path = _resolve_split_path(base, "val", deidentified_prefix)
    test_path = _resolve_split_path(base, "test", deidentified_prefix)

    train = _read_local_jsonl(train_path, max_rows=max_samples)
    val = _read_local_jsonl(val_path)
    test = _read_local_jsonl(test_path)
    return train, val, test


def deduplicate_examples(examples: list[dict]) -> list[dict]:
    """Drop exact duplicate ``(patient, doctor)`` pairs while preserving order."""
    seen: set[tuple[str, str]] = set()
    deduped: list[dict] = []
    for ex in examples:
        key = (
            clean_text(str(ex.get("patient", ""))).casefold(),
            clean_text(str(ex.get("doctor", ""))).casefold(),
        )
        if not key[0] or not key[1] or key in seen:
            continue
        seen.add(key)
        deduped.append(ex)
    return deduped


