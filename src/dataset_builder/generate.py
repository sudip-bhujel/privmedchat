"""Generate preference pairs for reward-model training.

Strategy: Expert vs. Non-Expert Pairing
- Chosen  = original doctor response from MedDialog (real medical expert).
- Rejected = response generated by a general-purpose base LLM (not medically
  fine-tuned) given the same patient prompt.

Base models produce generic, hedged, incomplete, or hallucinated responses
compared to expert doctors — creating strong contrast without human annotation.

Post-processing: pairs where chosen/rejected are too similar (cosine
similarity >= threshold) are filtered out to ensure a meaningful reward signal.
"""

