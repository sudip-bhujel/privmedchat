# Minimum word count for a rejected response to be kept.
_MIN_REJECTED_WORDS = 10
# Maximum word count for a rejected response to be kept.
_MAX_REJECTED_WORDS = 220

# Maximum number of tokens for patient text in the generation prompt.
# Very long patient texts confuse base LLMs and cause them to continue
# the patient narrative instead of switching to the doctor role.
_MAX_PATIENT_TOKENS = 512

# Instruction prefix to steer the base LLM into the doctor role.
_SYSTEM_INSTRUCTION = (
    "Below is a medical conversation. A patient describes their symptoms "
    "or asks a health question. Respond as a general medical assistant with "
    "concise, high-level guidance in 3-5 sentences. Avoid detailed "
    "differential diagnosis or step-by-step treatment plans.\n\n"
)

